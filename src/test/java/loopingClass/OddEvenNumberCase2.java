package loopingClass;

public class OddEvenNumberCase2 {

	public static void main(String[] args) {
		// Odd Even Number printing 
		int i = 1;
		while (i <= 10) {
            if (i % 2 == 0) {
                System.out.println(i + " is Even");
            } else {
                System.out.println(i + " is Odd");
            }
            i++; // Standard increment operator
        }
	

	}

}
